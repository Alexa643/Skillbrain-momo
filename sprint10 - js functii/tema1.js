function findSquare(num) {
    return num * num;
}

// Example usage with numbers:
let num1 = 4;
let num2 = 7;
let num3 = 15;

console.log("The square of " + num1 + " is: " + findSquare(num1));
console.log("The square of " + num2 + " is: " + findSquare(num2));
console.log("The square of " + num3 + " is: " + findSquare(num3));
